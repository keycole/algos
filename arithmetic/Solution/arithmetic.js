let result = 0;

const add = (num1, num2) => {
    result = num1 + num2;
    return result;
};

const subtract = (num1, num2) => {
    result = num1 - num2;
    return result;
};

const multiply = (num1, num2) => {
    result = num1 * num2;
    return result;
};

const divide = (num1, num2) => {
    result = num1 / num2;
    return result;
};
